# 构建城市查找字典
def get_city_list():
    """获取所有城市列表（用于下拉选择）"""
    return [f"{prov} {city}" for prov, city, _, _ in CITY_DATABASE]

def get_city_coordinates(province_city):
    """
    根据省份城市名称获取经纬度
    
    Args:
        province_city: 格式为"省份 城市"的字符串
        
    Returns:
        (longitude, latitude) 元组，如果找不到返回None
    """
    for prov, city, lon, lat in CITY_DATABASE:
        if f"{prov} {city}" == province_city:
            return (lon, lat)
    return None

def search_city(city_name):
    """
    根据城市名搜索（模糊匹配）
    
    Args:
        city_name: 城市名称
        
    Returns:
        匹配的城市列表 [(省份, 城市, 经度, 纬度), ...]
    """
    results = []
    for prov, city, lon, lat in CITY_DATABASE:
        if city_name in city or city in city_name:
            results.append((prov, city, lon, lat))
    return results

# 按省份分组的城市数据
def get_cities_by_province():
    """按省份分组获取城市"""
    provinces = {}
    for prov, city, lon, lat in CITY_DATABASE:
        if prov not in provinces:
            provinces[prov] = []
        provinces[prov].append({
            'city': city,
            'longitude': lon,
            'latitude': lat
        })
    return provinces


def get_provinces():
    """获取所有省份列表"""
    provinces = sorted(set([prov for prov, _, _, _ in CITY_DATABASE]))
    return provinces


