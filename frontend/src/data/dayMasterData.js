/**
 * 获取六十日柱身份
 * @param {string} dayPillar - 日柱（如"甲子"）
 * @param {string} gender - 性别
 * @returns {string} 身份文字
 */
export function getDayColumnIdentity(dayPillar, gender) {
  const data = DAY_COLUMN_DATABASE[dayPillar]
  if (!data) return ''
  const isMale = (gender === 'male' || gender === '男')
  return data[isMale ? 'male' : 'female']?.identity || ''
}

/**
 * 获取六十日柱描述
 * @param {string} dayPillar - 日柱（如"甲子"）
 * @param {string} gender - 性别
 * @returns {string} 描述文字
 */
export function getDayColumnDescription(dayPillar, gender) {
  const data = DAY_COLUMN_DATABASE[dayPillar]
  if (!data) return ''
  const isMale = (gender === 'male' || gender === '男')
  return data[isMale ? 'male' : 'female']?.description || ''
}

// 兼容旧接口：获取日柱完整信息（身份+描述）
export function getDayColumnSummary(dayPillar, gender) {
  const identity = getDayColumnIdentity(dayPillar, gender)
  const description = getDayColumnDescription(dayPillar, gender)
  if (!identity && !description) return ''
  return `${identity}。${description}`
}

